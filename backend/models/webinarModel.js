import mongoose from "mongoose";

const webinarSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    pic: {
        type: String,
        required: true,
      },
    category: {
      type: String,
      required: true,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
  },
  {
    timestamps: true,
  }
);

const Webinar = mongoose.model("Webinar", webinarSchema);

export default Webinar;
